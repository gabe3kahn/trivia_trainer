#!/usr/bin/env node
/**
 * Regenerate planning/clue-rewrite-lessons.md from the condensed corpus (clue-rewrite-lessons.jsonl).
 * Curated PRINCIPLE headers (stable) + EXEMPLARS pulled live from the corpus. We keep exemplars
 * AGGRESSIVELY — variety of flavor beats brevity, more beats fewer — deduping only near-identical
 * ones (same answer, or the same one-line lesson). Difficulty-calibration edits (section 7) ARE in
 * scope now — the editor's difficulty rewrites are the drafter's best signal for writing oblique,
 * value-matched clues instead of fact-stacked ones; this file is about writing good clues.
 *
 *   node tools/acquisition/build-rewrite-lessons.mjs   (after condense-rewrites)
 */
import fs from 'node:fs';

const loadJsonl = (p) => (fs.existsSync(p) ? fs.readFileSync(p, 'utf8').trim().split('\n').filter(Boolean).map((l) => JSON.parse(l)) : []);
const corpus = loadJsonl('data/acquisition/clue-rewrite-lessons.jsonl');
// Restraint signal: clues the editor left UNCHANGED (labeled-clues.jsonl, action=keep) so the drafter
// learns when NOT to edit — the eval showed the file was edit-only, biasing toward over-editing.
const keeps = loadJsonl('data/acquisition/labeled-clues.jsonl').filter((r) => r.action === 'keep');
// Editor's outcome-routed judgments on proposed rewrites (bank-rewrite feedback seed).
const fb118 = loadJsonl('data/acquisition/feedback-118.jsonl');

const SECTIONS = [
  { title: '1. Tighten the wording', types: ['tighten_wording', 'fill_trim'],
    guide: "Cut every word that doesn't earn its place; compress a multi-word descriptor into one precise word. The final clue is usually SHORTER. **Guardrail:** cut only GENUINE bloat — never strip color, flow, or an elegant cadence, and a SHORT clue can keep descriptive words that carry it. Brevity is a means, not the goal; if the clue already reads well, leave it." },
  { title: '2. Lead with a vivid, concrete hook — not provenance or category labels', types: ['add_vivid_hook', 'concrete_over_abstract'],
    guide: "Replace biography, provenance, and generic category descriptions with a specific work, a visual image, or a memorable nickname. Show the thing; don't catalog facts about it. **Guardrail:** the hook must NEVER add a famous work/title or detail that gives the answer away or makes the clue easier — that's a giveaway, not a hook. Preserve the difficulty." },
  { title: '3. Cut the giveaway — and hidden or phonetic leaks', types: ['cut_giveaway', 'cut_leak'],
    guide: "Remove any detail that reveals or phonetically implies the answer: a quote containing the answer word, an etymological root, an ordinal rank that telegraphs it." },
  { title: '4. Un-bury the subject', types: ['unbury_subject'],
    guide: "Don't name the answer's creator/location in a leading subordinate phrase; restructure so \"this [noun]\" clearly marks the answer and the identifying detail comes AFTER. **Guardrail:** only when the burial genuinely obscures — a strong, clear lede can precede the subject, so don't un-bury mechanically." },
  { title: '5. Grammar & polish', types: ['fix_grammar'],
    guide: 'Match the clue\'s tense to the event (a past event takes past tense — "relied," not "relies"). Keep a comma/semicolon OUTSIDE a title\'s quotes (\'Jazz Age\', not \'Jazz Age,\'). Never join two independent clauses with a bare comma (comma splice). A brief role label helps when citing a less-familiar authority ("the historian A.J.P. Taylor…"). (The punctuation-inside-quote and comma-splice cases are also caught deterministically.)' },
  { title: '6. Dedupe across the whole run', types: ['dedup_reword'],
    guide: 'When the standard angle duplicates another clue in the run or the bank, pivot to an unexpected detail that still uniquely identifies the subject — or cut it.' },
  { title: '7. Calibrate difficulty to the value — obliqueness, NOT fact-stacking', types: ['reframe_difficulty', 'value_up', 'value_down'],
    guide: "Difficulty comes from HOW OBLIQUE the hook is, never from piling on facts. For a HIGH-value clue ($800–1000), discard the famous summary/nickname and anchor on a lesser-known detail, an obscure endpoint or supporting character, or a surprising origin/reception fact that demands real subject knowledge. A LOW-value clue ($200–400) can use a direct hook. Never lead with the giveaway nickname, and never stack multiple identifying facts to pad a clue — one well-chosen oblique hook beats three obvious ones. Don't define an answer as the missing member of a short list, and cut trivia-within-trivia that inflates difficulty past the stated value without aiding recognition. Write each clue so its obliqueness matches its dollar value." },
];
const CAP = 10;
const clip = (s, n = 200) => (s.length > n ? s.slice(0, n) + '…' : s);

const used = new Set();
const out = [
  '# Clue rewrite lessons — learned from what review actually changed', '',
  `_Auto-generated by \`build-rewrite-lessons\` from ${corpus.length} real draft→final rewrites (git-derived; the`,
  'edit is the source of truth, the editor\'s review comment a supplementary hint). Regenerated each drafter run so',
  'it reflects every merged edit. Pre-apply these so the drafter writes the FINAL version on the first pass._', '',
  '> The top edits — tighten, vivid hook, cut giveaway, concrete-over-abstract — are the ones no deterministic gate',
  '> can catch. This is where they get taught.', '',
  '> **Calibrate difficulty as you draft (section 7).** Write each clue so its obliqueness matches the value you assign it —',
  '> oblique/single-hook for high value, direct for low — never pad difficulty by stacking facts or leading with a giveaway.', '',
  '> **These guardrails constrain HOW you rewrite, not WHETHER.** Propose a rewrite whenever it clearly IMPROVES the',
  '> clue — a hedged/generic opener, a subject buried so it obscures, real wordiness, a leak, a grammar error. Do not',
  "> withhold a clear improvement, and do not make lateral/trivial edits that don't improve it. When you DO rewrite,",
  '> apply the guardrails so the fix never backfires: it must not cost difficulty, flow, or elegance.', '',
];

for (const s of SECTIONS) {
  // Assign each pair to the section of its PRIMARY (first) change-type, so an exemplar sits under
  // the principle that actually drove its edit.
  const primaryIn = (c) => s.types.includes((c.change_types || [])[0]);
  const seenLesson = new Set();
  const picks = [];
  for (const c of corpus) {
    if (used.has(c) || !primaryIn(c)) continue;
    const lk = String(c.lesson || '').toLowerCase().slice(0, 40);
    if (seenLesson.has(lk) || seenLesson.has(c.answer)) continue; // dedupe near-identical flavor / same subject
    seenLesson.add(lk); seenLesson.add(c.answer);
    picks.push(c); used.add(c);
    if (picks.length >= CAP) break;
  }
  const n = corpus.filter(primaryIn).length;
  out.push(`## ${s.title} (${n})`, s.guide, '');
  for (const p of picks) {
    out.push(`- **${p.answer}** — ${p.lesson}`);
    out.push(`  - before: ${clip(p.before)}`);
    out.push(`  - after:  ${clip(p.after)}`);
  }
  out.push('');
}

// RESTRAINT section — clues left unchanged (up to 2 per category for a diverse spread).
if (keeps.length) {
  const byCat = {};
  const picks = [];
  for (const k of keeps) { byCat[k.category] = (byCat[k.category] || 0) + 1; if (byCat[k.category] <= 2) picks.push(k); }
  out.push('## Left unchanged — already good, do NOT over-edit these', 'These clues passed review with no change. If a clue already reads well, LEAVE IT ALONE — most clues need no edit.', '');
  for (const k of picks.slice(0, 18)) out.push(`- **${k.answer}** [${k.category}]: ${clip(k.before)}`);
  out.push('');
}

// EDITOR-REVIEW section — outcome-routed judgments on proposed rewrites (emulate approvals, leave rejects alone, apply tweaks).
if (fb118.length) {
  const ap = fb118.filter((f) => f.outcome === 'approve'), rj = fb118.filter((f) => f.outcome === 'reject'), tw = fb118.filter((f) => f.outcome === 'tweak');
  out.push('## From editor review of proposed rewrites', 'How the editor judged specific proposals — emulate the approved rewrites, do NOT make the kind that were rejected, apply the refinements.', '');
  if (ap.length) { out.push('**Approved rewrites (emulate this kind of improvement):**'); for (const f of ap.slice(0, 12)) out.push(`- **${f.answer}**: ${clip(f.before)}\n  → ${clip(f.proposed)}`); out.push(''); }
  if (rj.length) { out.push('**Rewrite REJECTED — editor kept the original (do NOT over-edit clues like these):**'); for (const f of rj) out.push(`- **${f.answer}**: ${clip(f.before)}`); out.push(''); }
  if (tw.length) { out.push('**Refined — editor accepted the rewrite with a tweak:**'); for (const f of tw) out.push(`- **${f.answer}**: ${clip(f.proposed)}  _(${clip(f.comment, 110)})_`); out.push(''); }
}

fs.writeFileSync('planning/clue-rewrite-lessons.md', out.join('\n').replace(/\n{3,}/g, '\n\n') + '\n');
console.log(`Wrote planning/clue-rewrite-lessons.md: ${SECTIONS.length} principle sections + ${keeps.length ? 'restraint(keeps)' : 'no-keeps'} + ${fb118.length} editor-review examples.`);
