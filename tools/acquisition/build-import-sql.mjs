import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { formatDifficultyCounts, formatQualityCounts } from './acquisition-utils.mjs';
import { assessQuestionForIntake } from './intake-assessment.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, '..', '..');
const bankPath = process.argv[2];
const outputPath = process.argv[3] ?? path.join(rootDir, 'data', 'acquisition', 'sql', 'import-questions.sql');

if (!bankPath) {
  throw new Error('Usage: node tools/acquisition/build-import-sql.mjs <normalized-question-bank.json> [output.sql]');
}

const bank = JSON.parse(await fs.readFile(path.resolve(bankPath), 'utf8'));
const questions = Array.isArray(bank) ? bank : bank.questions;
if (!Array.isArray(questions)) {
  throw new Error('Question bank must be an array or an object with a questions array.');
}

await fs.mkdir(path.dirname(outputPath), { recursive: true });

const qualityCounts = {};
const difficultyCounts = {};
const values = questions.map((question) => {
  const { prepared, quality, active, difficulty } = assessQuestionForIntake(question);
  qualityCounts[quality.decision] = (qualityCounts[quality.decision] ?? 0) + 1;
  if (difficulty.applied_value !== question.value) {
    const key = `$${question.value}->$${difficulty.applied_value}`;
    difficultyCounts[key] = (difficultyCounts[key] ?? 0) + 1;
  }

  return `    (${[
    sqlString(prepared.source),
    sqlString(prepared.source_url ?? null),
    sqlString(prepared.external_id),
    sqlString(prepared.category_id),
    sqlString(prepared.subcategory_name),
    sqlNumber(prepared.value),
    sqlNumber(prepared.difficulty_rank),
    sqlString(prepared.mechanic ?? 'standard'),
    sqlString(prepared.constraint_text ?? null),
    sqlString(prepared.clue),
    sqlString(prepared.answer),
    sqlArray(prepared.aliases ?? []),
    sqlArray(prepared.tags ?? []),
    sqlString(quality.decision),
    sqlNumber(quality.score),
    sqlArray(quality.issues),
    sqlBool(active)
  ].join(', ')})`;
});

const sql = `-- Generated by tools/acquisition/build-import-sql.mjs.
-- Review before running. Provider content may carry license/attribution obligations.

insert into questions (
  source,
  source_url,
  external_id,
  category_id,
  subcategory_id,
  value,
  difficulty_rank,
  mechanic,
  constraint_text,
  clue,
  answer,
  aliases,
  tags,
  quality_status,
  quality_score,
  quality_issues,
  is_active
)
select
  seed.source,
  seed.source_url,
  seed.external_id,
  seed.category_id,
  s.id,
  seed.value,
  seed.difficulty_rank,
  seed.mechanic,
  seed.constraint_text,
  seed.clue,
  seed.answer,
  seed.aliases,
  seed.tags,
  seed.quality_status,
  seed.quality_score,
  seed.quality_issues,
  seed.is_active
from (
  values
${values.join(',\n')}
) as seed(source, source_url, external_id, category_id, subcategory_name, value, difficulty_rank, mechanic, constraint_text, clue, answer, aliases, tags, quality_status, quality_score, quality_issues, is_active)
join subcategories s
  on s.category_id = seed.category_id
 and s.name = seed.subcategory_name
on conflict (source, external_id) where external_id is not null do update set
  source_url = excluded.source_url,
  category_id = excluded.category_id,
  subcategory_id = excluded.subcategory_id,
  value = excluded.value,
  difficulty_rank = excluded.difficulty_rank,
  mechanic = excluded.mechanic,
  constraint_text = excluded.constraint_text,
  clue = excluded.clue,
  answer = excluded.answer,
  aliases = excluded.aliases,
  tags = excluded.tags,
  quality_status = excluded.quality_status,
  quality_score = excluded.quality_score,
  quality_issues = excluded.quality_issues,
  is_active = excluded.is_active;
`;

await fs.writeFile(outputPath, sql);
console.log(`Wrote ${outputPath}`);
console.log(`Included ${questions.length} questions.`);
console.log(`Quality gate: ${formatQualityCounts(qualityCounts)}`);
console.log(`Difficulty calibration: ${formatDifficultyCounts(difficultyCounts)}`);

function sqlString(value) {
  if (value === null || value === undefined) return 'null';
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlNumber(value) {
  if (!Number.isFinite(Number(value))) throw new Error(`Invalid number: ${value}`);
  return String(Number(value));
}

function sqlArray(values) {
  if (!Array.isArray(values) || values.length === 0) return "'{}'::text[]";
  return `array[${values.map(sqlString).join(', ')}]::text[]`;
}

function sqlBool(value) {
  return value ? 'true' : 'false';
}
